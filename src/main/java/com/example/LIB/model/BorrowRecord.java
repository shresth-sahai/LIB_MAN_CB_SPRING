package com.example.LIB.model;

import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection = "borrow_records")
public class BorrowRecord {
    private String id;
    private String bookId; private String userId;
    private String borrowDate;    private String returnDate;
     public BorrowRecord(){}

    public BorrowRecord(String id, String bookId, String userId, String borrowDate, String returnDate) {
        this.id = id;
        this.bookId = bookId;
        this.userId = userId;
        this.borrowDate = borrowDate;
        this.returnDate = returnDate;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBookId() {
        return bookId;
    }

    public void setBookId(String bookId) {
        this.bookId = bookId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getBorrowDate() {
        return borrowDate;
    }

    public void setBorrowDate(String borrowDate) {
        this.borrowDate = borrowDate;
    }

    public String getReturnDate() {
        return returnDate;
    }

    public void setReturnDate(String returnDate) {
        this.returnDate = returnDate;
    }
}
